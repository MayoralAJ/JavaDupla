// 5) DO...WHILE

// Parecido com o while, mas garante que executa pelo menos uma vez
let numero = 1;

console.log("Contagem com do...while:");
do {
 console.log(numero); // mostra o valor
 numero++; // incrementa, ou seja, 2, até chegar em 5, linha seguinte

} while (numero <= 5); // verifica a condição

//a diferença entre o while e do-while é que o do-while executa o código primeiro e depois verifica a condição, já o while, verifica a condição antes de executar
